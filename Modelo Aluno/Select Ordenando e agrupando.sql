ON A.COD_ALUNO = AD1.COD_ALUNO 
INNER JOIN TB_ALUNO_DISCIPLINA AD2
ON A.COD_ALUNO = AD2.COD_ALUNO 
WHERE AD1.ID_DISCIPLINA ='POR' 
    AND AD2.ID_DISCIPLINA ='MAT';